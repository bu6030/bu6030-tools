for i, match in enumerate(matches[:5]):  # 只打印前5个进行检查
    print(f'匹配 {i+1}: {match}')
print('...')
if len(matches) > 5:
    print(f'最后几个匹配:')
    for i in range(max(0, len(matches)-3), len(matches)):
        print(f'匹配 {i+1}: {matches[i]}')


# 构建 JSON 数据
