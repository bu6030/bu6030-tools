# 使用正则表达式提取日期和利率数据
# 修改正则表达式以匹配带空格或不带空格的格式
pattern = r'(\d{4}/\d{2}/\d{2})：1年期\s*([\d.]+)%，5年以上\s*([\d.]+)%'
matches = re.findall(pattern, data_string)

# 打印匹配结果以调试
